    private void jButtonAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAbrirActionPerformed
        String Idbn,Titulo,Autor,A単o,Costo,Paginas,Edicion,Duracion,Formato;
        String au = "Registro.pb";
        Scanner linea = null;
        File doc_le = new File(au);
        try {
            linea = new Scanner(doc_le);
            while (linea.hasNextLine()) {                
                Idbn = linea.nextLine();
                Titulo = linea.nextLine();
                Autor = linea.nextLine();
                A単o = linea.nextLine();
                Costo = linea.nextLine();
                Paginas = linea.nextLine();
                Edicion = linea.nextLine();
                Duracion = linea.nextLine();
                Formato = linea.nextLine();
                model.addRow(new Object[]{Idbn,Titulo,Autor,A単o,Costo,Paginas,Edicion,Duracion,Formato});
                
            }
        } catch (Exception x) {
            JOptionPane.showMessageDialog(null, "Error");
        }
    }//GEN-LAST:event_jButtonAbrirActionPerformed

